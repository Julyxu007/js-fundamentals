// = assignment operator
// == comparison operator (compare if the values are equal)
// === strict equality operator (compare if the values and the data types are equal)
// != inequality operator (compare if the values are not equal)
// !== strict inequality operator (compare if the values and the data types are not equal)

const PI = 3.14159;
if (PI === "3.14159") {
  console.log("PI is equal to 3.14159");
} // false
if (PI == "3.14159") {
    console.log("PI is equal to 3.14159 as a string");
} // true
